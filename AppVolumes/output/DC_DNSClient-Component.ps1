Trap [Exception]
	{
	 # Handle exception and throw it to the stdout log file. Then continue with function and script.
		 $Script:ExceptionMessage = $_
		 "[info]: Exception occurred."  | WriteTo-StdOut
		 "[info]: Exception.Message $ExceptionMessage."  | WriteTo-StdOut 
		 $Error.Clear()
		 continue
		 # later use return to return the exception message to an object:   return $Script:ExceptionMessage
	}

Import-LocalizedData -BindingVariable ScriptVariable
Write-DiagProgress -Activity $ScriptVariable.ID_CTSDNSClient -Status $ScriptVariable.ID_CTSDNSClientDescription


function RunNetSH ([string]$NetSHCommandToExecute="")
{
	Write-DiagProgress -Activity $ScriptVariable.ID_CTSDNSClient -Status "netsh $NetSHCommandToExecute"
	$NetSHCommandToExecuteLength = $NetSHCommandToExecute.Length + 6
	"-" * ($NetSHCommandToExecuteLength) + "`r`n" + "netsh $NetSHCommandToExecute" + "`r`n" + "-" * ($NetSHCommandToExecuteLength) | Out-File -FilePath $outputFile  -append
	$CommandToExecute = $Env:windir + "\system32\cmd.exe /c netsh.exe " + $NetSHCommandToExecute + " >> `"$outputFile `""
	RunCmD -commandToRun $CommandToExecute  -CollectFiles $false  -BackgroundExecution
}

function runPS ([string]$runPScmd="")
{
	$runPScmdLength = $runPScmd.Length
	"-" * ($runPScmdLength) | Out-File -FilePath $outputFile -append
	"$runPScmd" | Out-File -FilePath $outputFile -append
	"-" * ($runPScmdLength) | Out-File -FilePath $outputFile -append
	Invoke-Expression $runPScmd | out-file -FilePath $outputFile -append
}


#----------Registry
$outputFile = $Computername + "_DnsClient_reg_.TXT"
$CurrentVersionKeys = "HKLM\SOFTWARE\Policies\Microsoft\Windows NT\DNSClient",
						"HKLM\SYSTEM\CurrentControlSet\services\Dnscache"
$sectionDescription = "DNS Client"
RegQuery -RegistryKeys $CurrentVersionKeys -Recursive $true -OutputFile $outputFile -fileDescription "DnsClient Registry Keys" -SectionDescription $sectionDescription


#----------Ipconfig /displaydns
$outputFile = join-path $pwd.path ($ComputerName + "_DnsClient_ipconfig-displaydns.TXT")
$CommandToExecute = $Env:windir + "\system32\cmd.exe /c ipconfig.exe /displaydns >> `"$outputFile `""

RunCmD -commandToRun $CommandToExecute -CollectFiles $false -BackgroundExecution
CollectFiles -filesToCollect $outputFile -fileDescription "Ipconfig /displaydns" -SectionDescription $sectionDescription


#----------Copy the HOSTS file
$outputFile = $ComputerName + "_DnsClient_HostsFile.TXT"
$hosts = "$ENV:windir\system32\drivers\etc\HOSTS"
if (test-path $hosts)
{
  Copy-Item -Path $hosts -Destination $outputFile 
  CollectFiles -filesToCollect $outputFile -fileDescription "DNS Client - HOSTS file" -SectionDescription $sectionDescription
}
else
{
  "$hosts Does not exist" | writeto-stdout
}

